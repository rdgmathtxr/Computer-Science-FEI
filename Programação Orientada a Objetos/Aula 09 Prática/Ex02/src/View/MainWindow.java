    private void jButtonShowClientsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonShowClientsActionPerformed
        jTextAreaData.setText("");
        
        PeopleController controller = new PeopleController();
        ArrayList<Cliente> clients = controller.getClientes();
        
        if (clients.isEmpty())
            jTextAreaData.append("Nenhum cliente a ser exibido");
        else{
            for(int i = 0; i < clients.size(); i++){
                Cliente client = (Cliente) clients.get(i);
                jTextAreaData.append("======================================\n");
                jTextAreaData.append("                Cliente " + (i+1) + "\n");
                jTextAreaData.append("======================================\n");
                
                jTextAreaData.append(client.toString());

                for(int j = 0; j < client.getAnimais().size(); j++){
                    Animal pet = client.getAnimais().get(j);
                    String type = null;
                    if (pet.getClass() == Cachorro.class)
                        type = "Cachorro";
                    else if (pet.getClass() == Gato.class)
                        type = "Gato";
                    
                    jTextAreaData.append("\nAnimal " + (j+1) + ": "+type+ "\n");
                    jTextAreaData.append(pet.toString());
                }
                
                jTextAreaData.append("======================================\n");
            }
        }
        
        
    }//GEN-LAST:event_jButtonShowClientsActionPerformed

