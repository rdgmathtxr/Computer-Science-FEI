    private void btCadastraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastraActionPerformed
        String nome = input1.getText();
        String sobrenome = input2.getText();
        String idade = input3.getText();
        String cpf = input4.getText();
        String sexo;
        if (masculino.isSelected()){
            sexo = "masculino";
        }
        else if (masculino.isSelected()){
            sexo = "feminino";
        }
        else{
            sexo = "nao selecionado";
        }
        File arquivo = new File(nome, sobrenome, cpf, idade, sexo);
        arquivo.addDataToFile();
    }//GEN-LAST:event_btCadastraActionPerformed

