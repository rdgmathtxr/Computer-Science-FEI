nota = input("Digite a nota: ")

if nota == "A+":
    print("O número de pontos é 4.0")
elif nota == "A":
    print("O número de pontos é 4.0")
elif nota == "A-":
    print("O número de pontos é 3.7")
elif nota == "B+":
    print("O número de pontos é 3.3")
elif nota == "B":
    print("O número de pontos é 3.0")
elif nota == "B-":
    print("O número de pontos é 2.7")
elif nota == "C+":
    print("O número de pontos é 2.3")
elif nota == "C":
    print("O número de pontos é 2.0")
elif nota == "C-":
    print("O número de pontos é 1.7")
elif nota == "D+":
    print("O número de pontos é 1.3")
elif nota == "D":
    print("O número de pontos é 1.0")
elif nota == "F":
    print("O número de pontos é 0.0")
else:
    print("A nota digitada não é válida: ", nota)


